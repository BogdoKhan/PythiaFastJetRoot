#!/bin/bash

nasada=1508000               #serie
jetR="0.5"  
############################################
ipp=0

while [ $ipp -lt 49 ]; do 
   rndNum=$((nasada+ipp))
   echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++"
   echo "./pysec.exe $rndNum $jetR"
   echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++"
   ./pysec.exe $rndNum $jetR 

   ipp=$((ipp+1)) 
done

